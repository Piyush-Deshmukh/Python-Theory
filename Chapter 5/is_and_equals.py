fruits1 = ["Grapes", "Apple", "Orange"]
fruits2 = ["Bananas", "Mango"]
fruits3 = ["Grapes", "Apple", "Orange"]

# equals --> Determines if Lists are Equal or Not
print(fruits1 == fruits2)
print(fruits1 == fruits3)

# is --> Determines if lists are at same Memory Place or Not
print(fruits1 is fruits2)
print(fruits1 is fruits3)