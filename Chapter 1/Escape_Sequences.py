# Escape Sequences :
# \'  --> Single Quote
# \"  --> Double Quote
# \\  --> Backslash
# \n  --> New Line
# \t  --> Tab
# \b  --> Backspace 

print("Hi I am \"Escape Sequence\"")
print("Hello\nWorld")
print("This is Backslash \\")
print("Hello\tWorld")
print("Helll\bo")
print("This is Double Backslash \\\\")
print("This is Escape Sequence for New Line \\n")
print(" \\\" \\\'")


print(r"Hello \n World") # Raw String
