print(5+2)  # Addition
print(5-2)  # Subtraction
print(5*2)  # Multiplication
print(5/2)  # Division giving Float value
print(5//2) # Division giving Integer value
print(5**2) # 5 power 2 = 25
print(5%2)  # Remainder

print(2**0.5) # SquareRoot
print(round(2**0.5,4)) # Rounding off to 4 Decimal Places


# Operation        Description       Associativity

#   ()             Parenthesis       Left to Right
#   **              Exponent         Right to Left
# *,/,//,%                           Left to Right
#   +,-                              Left to Right

print((2+3)-1*25//4%2/2)
print(2**3**2)